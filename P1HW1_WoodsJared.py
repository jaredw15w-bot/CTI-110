# -----Calculating Exponents-----

print("-----Calculating Exponents-----\n")

base_value = int(input("Enter an integer as the base value: "))
exponent = int(input("Enter an integer as the exponent: "))

result = base_value ** exponent

print()
print(f"{base_value} raised to the power of {exponent} is {result} !!\n")

# -----Addition and Subtraction-----

print("-----Addition and Subtraction-----\n")

starting_integer = int(input("Enter a starting integer: "))
add_integer = int(input("Enter an integer to add: "))
subtract_integer = int(input("Enter an integer to subtract: "))

final_result = starting_integer + add_integer - subtract_integer

print()
print(f"{starting_integer} + {add_integer} - {subtract_integer} is equal to {final_result}")

